const helloWorld = function() {
    return "Hello, World!";
}

function sayHello(x) {
    let hello = "Hello, "

    if (x === "Jane") {
        return hello + "Jane!"
    }
    else if (x === "Alex") {
        return hello + "Alex!"
    }
    else if (x === "Pat") {
        return hello + "Pat!"
    }
    else if (x === "World") {
        return hello + "World!"
    }
}


